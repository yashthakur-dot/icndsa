  <h1>Instructions for Authors</h1>
  <hr>

  <ul class="instructions-list text-justify">
    <li>
      Authors are required to submit their manuscripts using Microsoft CMT by clicking
      <a href="https://cmt3.research.microsoft.com/ICNDSA2026">here</a>.
    </li>

    <li>
      Please ensure that your manuscript adheres to the specified length requirement
      of 5–6 pages, following the IEEE Conference format guidelines. You can download
      the template from this link:- <a href="https://www.ieee.org/conferences/publishing/templates.html">Template</a>.
    </li>

    <li>
      The submission of manuscripts should be done in PDF format only.
    </li>

    <li>
      All submitted papers will undergo a rigorous review process. Acceptance decisions
      will be made based on criteria including quality, originality, technical content,
      and relevance to the conference's theme.
    </li>

    <li>
      Every paper submitted to ICNDSA-2026 will undergo plagiarism detection using
      anti-plagiarism software prior to review. Papers found to not meet the plagiarism
      policy may be desk-rejected without further review.
    </li>

    <li>
      Only registered and presented papers will be forwarded to IEEE for inclusion in
      the IEEE Xplore Digital Library. Note: IEEE’s ‘No Show’ policy will be enforced.
      Papers not presented at the conference will not be eligible for inclusion.
    </li>

    <li>
      ICNDSA-2026 strictly prohibits authors from altering their details or the sequence
      of authors once a manuscript has been submitted.
    </li>
  </ul>

</section>
    <h2 class="section-title">About University</h2>

    <p>
      CHRIST (Deemed to be University) was born out of the educational vision of
      St Kuriakose Elias Chavara, an educationalist and social reformer of the
      nineteenth century in South India. He founded the first Catholic
      indigenous congregation, Carmelites of Mary Immaculate (CMI), in 1831,
      which administers CHRIST (Deemed to be University).
    </p>

    <p>
      CHRIST was established on 15 July 1969 as an affiliated college, with the
      vision of <strong>Excellence and Service</strong>. It became a preferred
      educational institution in Bangalore, Delhi-NCR and Lavasa within a short
      period of its inception.
    </p>

    <p>
      One of the first institutions in India to be accredited in 1998 by NAAC,
      and subsequently in 2004 and 2016, CHRIST (Deemed to be University) has the
      top grade <strong>'A' in the 4-point scale</strong>. Today, the University
      offers Undergraduate, Postgraduate and PhD programmes in Humanities,
      Social Sciences, Science, Commerce, Management, Engineering, Education,
      Law and Architecture to over <strong>20,000 students</strong> from all
      states of India and about 60 different countries.
    </p>

    <p>
      In 2019, CHRIST (Deemed to be University) completed
      <strong>50 glorious years</strong> of its existence and sterling
      contribution to the cause of higher education in India.
    </p>
  </section>


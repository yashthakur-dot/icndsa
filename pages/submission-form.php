    <h2>Guidance for IEEE Publications Regarding AI-Generated Text</h2>

    <p>
      The use of artificial intelligence (AI)-generated text in an article shall
      be disclosed in the acknowledgements section of any paper submitted to an
      IEEE Conference or Periodical. The sections of the paper that use
      AI-generated text shall have a citation to the AI system used to generate
      the text.
    </p>

    <p>
      This and other submission policies can be found in the IEEE Author Center
      Conference Author Submission Policies page at:
